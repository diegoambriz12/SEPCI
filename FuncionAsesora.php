    <section>
        <h3>II. FUNCIONES DE LA PERSONA ASESORA </h3>
        <div class="content">
            <p>Las funciones de la Persona Asesora están fundamentadas en el Capítulo Cuarto del PROTOCOLO de actuación
                de los
                Comités de Ética y de Prevención de Conflictos de Interés en la atención de presuntos actos de
                Discriminación
                (D.O.F. 18/07/2017); con base en lo anterior debe:</p>

            <ul>
                <li><strong>a)</strong> Realizar el primer contacto con la Presunta Víctima;</li>
                <li><strong>b)</strong> Procurar, con los medios que le fueron conferidos, el derecho a la no
                    discriminación de
                    la Presunta Víctima y, en su caso, orientarla a las instancias competentes para hacer efectivo dicho
                    derecho;
                </li>
                <li><strong>c)</strong> Procurar seguridad y privacidad a la Presunta Víctima al momento de realizar la
                    entrevista inicial, en el lugar que se le proporcione;</li>
                <li><strong>d)</strong> Dar el Seguimiento correspondiente a las denuncias que le sean presentadas.</li>
                <li><strong>e)</strong> Establecer una relación empática con la Presunta Víctima, ser cuidadosa con el
                    tono de
                    su voz cuando se dirija a ésta, mantener un perfil bajo en su lenguaje corporal, además de ser
                    precavida
                    respecto del contacto físico;</li>
                <li><strong>f)</strong> Expresar con oportunidad a la Presunta Víctima, el alcance de su función y del
                    acompañamiento que puede otorgar, e incluso debe orientar a la Presunta Víctima, o cualquier persona
                    que
                    solicite la información, sobre las instancias que son competentes para conocer los hechos narrados,
                    así como
                    de aclarar que su presentación ante una u otra no limita su derecho de acudir a alguna otra a
                    presentar su
                    denuncia;</li>
                <li><strong>g)</strong> Mencionar a la Presunta Víctima que pueden requerirle para presentarse ante el
                    Subcomité
                    con la finalidad de aportar mayores elementos del caso;</li>
                <li><strong>h)</strong> Apoyar a la Presunta Víctima en el llenado del formato de denuncia, en la que se
                    incluya
                    en forma de narrativa, una descripción de las conductas, que contenga datos de identificación,
                    correo
                    electrónico y número de teléfono, en su caso, de domicilio, así como las circunstancias de tiempo,
                    modo, y
                    lugar y, en su caso, referencia de terceras personas a las puedan constar los hechos; siempre
                    cuidando la
                    estabilidad emocional de la Presunta Víctima, y procurando no revictimizarla;</li>
                <li><strong>i)</strong> Acompañar a la Presunta Víctima en la presentación de la denuncia ante el
                    Subcomité.
                </li>
                <li><strong>j)</strong> Solicitar a la Presidencia del Subcomité, se adopten medidas preventivas para la
                    protección de la integridad o buscar cesar con los actos que dieron origen a la denuncia;</li>
                <li><strong>k)</strong> Llevar un registro de los casos y de las denuncias presentadas, así como del
                    trámite que
                    se le dio a la misma, en el formato que establezca el Consejo, en su caso, debe otorgar información
                    sobre
                    éstas a las autoridades que así lo requieran;</li>
                <li><strong>l)</strong> Solicitar, cuando lo estime necesario, el auxilio de las o los asesores del
                    Comité, para
                    brindar orientación o canalizar a la Presunta Víctima a las instituciones adecuadas para interponer
                    su
                    denuncia o queja en las instancias competentes;</li>
                <li><strong>m)</strong> Atender los exhortos o llamados del Subcomité, para otorgar asesoría u opinión
                    sobre
                    casos de su competencia.</li>
            </ul>

        </div>
