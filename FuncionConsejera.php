                    declaración
                    respectiva;</li>
                <li><strong>d)</strong> Acompañar a la presunta víctima con la Secretaría del Subcomité de ética para la
                    presentación de la denuncia en un plazo no mayor a tres días hábiles;</li>
                <li><strong>e)</strong> Atender los exhortos o llamados del Subcomité, para otorgar asesoría u opinión
                    sobre
                    casos de hostigamiento sexual y acoso sexual;</li>
                <li><strong>f)</strong> Excusarse de intervenir en el supuesto de que pudiera actualizarse un Conflicto
                    de
                    interés en términos de las disposiciones jurídicas aplicables; o bien, actuar conforme a las
                    instrucciones que
                    reciba del Subcomité para actuar en determinado sentido;</li>
                <li><strong>g)</strong> Hacer del conocimiento por escrito al Comité de Ética y de Prevención de
                    Conflictos del
                    Interés (CEPCI) del TecNM, cuando el Subcomité o alguno de sus integrantes se niegue a realizar
                    acciones
                    derivadas de la aplicación del Protocolo, y al Órgano Interno de Control cuando la negativa sea del
                    propio
                    CEPCI;</li>
                <li><strong>h)</strong> Brindar atención a la Presunta víctima conforme a las disposiciones jurídicas
                    aplicables
                    y el Protocolo, sin que esto signifique una representación legal;</li>
                <li><strong>i)</strong> Turnar en un plazo no mayor a tres días hábiles a la Secretaría Ejecutiva del
                    Subcomité
